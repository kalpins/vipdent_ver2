Nākamajā zobārsta apmeklējuma reizē pacients ierodas, lai pielaikotu vaska zobu protēzes modeli. Tādā veidā tiek pārbaudīts, vai topošā zobu protēze pacientam būs pilnībā piemērota. Ja vaska modelis pacientam der, tad zobu tehniskajā laboratorijā tiek izgatavota īstā zobu protēze. Nepieciešamības gadījumā tiek veiktas korekcijas.
