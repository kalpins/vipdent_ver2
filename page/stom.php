Zobu protezēšana
Zobu protezēšana ļauj kvalitatīvi restaurēt dažādu iemeslu dēļ zaudētus dabīgos zobus, atjaunojot to funkcionalitāti un estētiskās īpašības. Zobu protezēšanā tiek izmantotas gan izņemamās zobu protēzes (zobu plates), gan neizņemamas konstrukcijas – zobu kroņi un zobu tilti.
